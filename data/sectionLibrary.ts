// Convenience lookups derived from the source data.
export const sortedCategories = [...sectionCategories].sort(
  (a, b) => a.order - b.order,
);

export function getCategory(categoryId: string): SectionCategory | undefined {
  return sectionCategories.find((c) => c.id === categoryId);
}

export function getSection(categoryId: string, sectionId: string) {
  return getCategory(categoryId)?.sections.find((s) => s.id === sectionId);
}
